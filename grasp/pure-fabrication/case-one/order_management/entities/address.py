class Address:

    def __init__(self,
                 address_value,
                 country,
                 city,
                 neighborhood,
                 details,
                 department,
                 responsible,
                 type_of_address):
        self.address_value = address_value
        self.country = country
        self.city = city
        self.neighborhood = neighborhood
        self.details = details
        self.department = department
        self.responsible = responsible
        self.type_of_address = type_of_address



